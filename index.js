function add(a, b) {
  return a + b
}

function suntract(a, b) {
  return a - b
}

function multiply(a, b) {
  return a * b
}

function divide(a, b) {
  return a / b
}  

function inc(number) {
  return number + 1
}

function dec(number) {
  return number - 1
}

function makeInt(number) {
  return parseInt(number, 10)
}
